    //I realized that just doing this try catch thing was crazy redundant so...
    //Function it goes!
    public void forcedPause(int pausetime)
    {
        try
        {
            java.util.concurrent.TimeUnit.MILLISECONDS.sleep(pausetime);
        } catch (InterruptedException e)
        {
        }
    }

    public void printStringSlow(String lyric, int delay)
    {

        char[] lyricHolder = lyric.toCharArray();

        for (int i = 0; i < lyricHolder.length; i++)
        {
            System.out.print(lyricHolder[i]);
            forcedPause(delay);
        }
        System.out.println("");
    }


